          <h3 style="max-width: none; text-align: left; font-size: 28px; margin-top: 50px;">Что такое торговый счет и для чего он нужен</h3>
          <p class="text__title">Чтобы стать полноправным участником торгов на фондовых биржах, необходимо открыть счет на торговой площадке у лицензированного брокера. Данная процедура является обязательной и без нее невозможно получить доступ к американским фондовым биржам. Счет для торговли акциями содержит данные о балансе, информацию о сделках и результатах операций купли-продажи.</p>
        </div>
        <div class="col-lg-12">
          <h3 style="max-width: none; text-align: left; font-size: 28px; margin-top: 20px;">Как открыть торговый счет на бирже?</h3>
          <p class="text__title">Для того, чтобы открыть торговый счет, необходимо обратиться к лицензированному брокеру, определиться с программным обеспечением, заполнить договор и внести депозит. Если вы не разбираетесь в брокерах, опасаетесь мошенников и необходима помощь - можете смело обращаться к нам.</p>
          <p class="text__title__bold">Преимущества открытия торгового счета у лицензированного брокера:</p>
          <ul>
            <li>лицензированное программное обеспечение и гарантия технической надежности платформ;</li>
            <li>прямой вывод сделок на рынок;</li>
            <li>выгодные торговые условия и кредитное плечо;</li>
            <li>помощь в обслуживании торговой платформы.</li>
          </ul>
          <h3 style="max-width: none; text-align: left; font-size: 28px;">Чем торговый счет отличается от демо счета на бирже?</h3>
          <p class="text__title">В целом, демо торговля на бирже существенно отличается от реальной. Торговый счет позволяет получить доступ на американские фондовые биржи и совершать сделки в реальном времени. Демо-счет предназначен для изучения интерфейса платформы и ознакомления с функционалом. Для торговли на бирже демонстрационный счет не подходит, так как в данном режиме отсутствует симуляция очереди при исполнении заказа, а также психологический фактор, который является важнейшей особенностью торговли на реальном счете.</p>
