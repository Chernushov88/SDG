          <h3 style="max-width: none; text-align: left; font-size: 28px; margin-top: 50px;">Что такое торговый счет и для чего он нужен</h3>
          <p class="text__title">Торговый счет можно открыть как для биржевой, так и для OTC (over the counter) торговли. Биржевые торги контролируются государственными и частными регуляторами, гарантируют исполнение обязательств и прозрачность процесса. При внебиржевом трейдинге (напр. Forex, бинарные опционы и др. ) не возможно проверить прозрачность, ведь биржи Forex не существует, а значит и регуляция очень ограничена. Этот формат влечет за собой массу всевозможных манипуляций и не рекомендован в связи с высокой вероятностью потери средств. Чтобы стать полноправным участником торгов, необходимо открыть счет на бирже в надежной фирме.</p>
          <p class="text__title">После активации индивидуальной учетной записи будет представлена подробная информация о балансе, комиссиях и результатах операций купли-продажи. Также, перед совершением сделок на реальном счету брокер может открыть демо по запросу клиента. Торговля акциями на демо позволит начинать знакомиться с интерфейсом платформы и горячими клавишами в первые дни практики.</p>
