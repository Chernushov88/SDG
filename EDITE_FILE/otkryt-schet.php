          <h3 style="max-width: none; text-align: left; font-size: 28px;">Чем торговый счет отличается от демо счета на бирже?</h3>
          <p class="text__title">В целом, демо торговля на бирже существенно отличается от реальной. Вложение денег предполагает повышенную концентрацию, психологическую нагрузку и не является развлечением, похожим на казино. Инвестору нужно систематически проводить тщательный анализ и сам процесс купли\продажи занимает не более 30% времени. Игра на демке не подразумевает рисков и очень часто влечет за собой т.н. "гэмблинг" - очень активную торговлю с минимумом анализа и\или несерьезным отношением к результату. Такой подход ни к чему не приведет и лишь создаст иллюзию простоты, чем пользуются недобросовестные компании на Forex и др. внебиржевых организациях, зазывая поскорее открыть торговый счет.</p>
          <p class="text__title">В случае более серьезного подхода человек, который у брокера открыл демо счет, пытается протестировать стратегию и также попадает в ловушку. Это связано с тем, что демка не подразумевает симуляцию очереди при исполнении ордеров, а также отсутствует часть данных (ведь маркет-дата от поставщика котировок платная и приходится урезать бесплатный функционал). Демо-счет на фондовом рынке предназначен исключительно для знакомства и изучения платформы. В дополнение, при торговле на бирже демо счет не включает психологический фактор, от которого на реале не избавиться.</p>
          <p class="text__title">Если вы чувствуете неуверенность в своих знаниях - лучше <a href="obuchenie" target="_blank" rel="noopener noreferrer">начать с обучения</a>, а не с демо счета на американской бирже. Мы подготовили записи вебинаров как для новичков, так и для опытных. Начальные видеоматериалы дадут возможность разобраться в отличии демо счета на фондовой бирже от полноценного; с какой суммы будет эффективно начать, и как не допустить самые популярные ошибки. Курсы продвинутого уровня помогут вам сформировать стратегию, научиться правильно распределять риски и отбирать ценные бумаги. Программу составляли специалисты с более чем 10-летним опытом. Торговля на фондовом рынке в режиме демо счета позволит закрепить полученные из курсов знания, что упростит освоение азов.</p>
